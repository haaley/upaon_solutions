        <!-- Seção de etapas do desenvolvimento -->
        <section id="etapas" class="study text-center wow fadeIn" data-wow-duration="2s" data-wow-dealy="1.5s">
            <div class="container">
                <div class="row">
                    <div class="main_study_area sections">
                        <div class="head_title text-center">
                            <h2>ETAPAS DO DESENVOLVIMENTO</h2>
                            <div class="subtitle">
                                Entenda como funciona o processo de desenvolvimento de um website.
                            </div>
                            <div class="separator"></div>
                        </div>
                        <div class="single_study_content">
                            <div class="col-sm-6">
                                <div class="single_study_slid_area">

                                    <div class="single_study_text">
                                        <div class="study_slider">
                                            <div class="item">
                                                <div class="s_study_icon">
                                                    <i class="far fa-comments"></i>
                                                </div>
                                                <h4>BRIENFING E CONVERSA COM CLIENTE</h4>
                                                <div class="separator3"></div>
                                                <p>É muito importante que fique claro entre criador e cliente qual o
                                                    objetivo por trás do site, qual a história da empresa, concorrentes,
                                                    referências, e todo um conjunto de informações que devem estar
                                                    completas para que o produto final seja eficiente quanto aos
                                                    objetivos e transpareça a imagem da empresa.</p>

                                            </div>
                                            <div class="item">
                                                <div class="s_study_icon">
                                                    <i class="fa fa-lightbulb-o"></i>
                                                </div>
                                                <h4>Conteúdo </h4>
                                                <div class="separator3"></div>
                                                <p>Criar conteúdo para um site não é simplesmente sentar e escrever
                                                    sobre a empresa e seus produtos e serviço.
                                                    Através da pesquisa de mercado e do briefing o conteúdo é
                                                    inicialmente criado, e depois otimizado através de técnicas e
                                                    ferramentas de SEO (Search Engine Optimization), que ajudam com que
                                                    o site seja encontrado através das palavras chaves do segmento em
                                                    que está inserido.</p>

                                            </div>
                                            <div class="item">
                                                <div class="s_study_icon">
                                                    <i class="fas fa-pencil-ruler"></i>
                                                </div>
                                                <h4>Layout + Design</h4>
                                                <div class="separator3"></div>
                                                <p> Após criado o conteúdo, é criado o mapa do site, também chamado de
                                                    Wireframe. O wireframe é uma espécie de “maquete” virtual do site,
                                                    ou seja, é um rascunho que desenha onde cada imagem, texto e botão
                                                    será inserido. Esta etapa é importante, pois é baseado neste
                                                    wireframe que o designer vai seguir com suas criações.
                                                </p>
                                            </div>
                                            <div class="item">
                                                <div class="s_study_icon">
                                                    <i class="fas fa-code"></i>
                                                </div>
                                                <h4> Codificação</h4>
                                                <div class="separator3"></div>
                                                <p>A etapa de programação consiste em codificar as funcionalidades já
                                                    definidas previamente, sempre baseando-se no wireframe e também no
                                                    design do site. Programar consiste em transformar as imagens e
                                                    layout criados pelo designer em códigos interpretáveis pelos
                                                    navegadores.
                                                </p>
                                            </div>
                                            <div class="item">
                                                <div class="s_study_icon">
                                                    <i class="fas fa-laptop-code"></i>
                                                </div>
                                                <h4> Programação + Testes
                                                </h4>
                                                <div class="separator3"></div>
                                                <p>É nesta etapa que os códigos escritos entram em ação de
                                                    fato, ou seja, são aplicados e criam a dinâmica do website. São
                                                    criados e incorporados sistemas e plugins necessários, além de todas
                                                    as funcionalidades do site, montando a primeira versão oficial do
